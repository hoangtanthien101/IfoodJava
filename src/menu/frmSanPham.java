    boolean kiemTraMon() {
        if (txtTenMonAn.getText().trim().length() == 0) {
            MsgBox.alert(this, "Tên món không được để trống!");
            txtTimKiem.requestFocus();
            return false;
        }
        if (txtGiaTien.getText().trim().length() == 0) {
            MsgBox.alert(this, "Giá tiền không được để trống!");
            txtGiaTien.requestFocus();
            return false;
        }
        try {
            double giaien = Double.parseDouble(txtGiaTien.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Giá tiền phải là số !");
            txtGiaTien.setText("");
            txtGiaTien.requestFocus();
            return false;
        }
        return true;
    }

