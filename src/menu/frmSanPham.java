    void fillTableLoai() {
        DefaultTableModel model = (DefaultTableModel) tblLoaiMonAn.getModel();
        model.setRowCount(0);
        try {
            List<LoaiMonAn> list = loaiDao.selectAll();
            for (LoaiMonAn loai : list) {
                Object[] rows = {loai.getMaLoaiMonAn(),
                    loai.getTenLoaiMonAn()};
                model.addRow(rows);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy cập dữ liệu!");
        }
    }

    private LoaiMonAn getFormLoai() {
        LoaiMonAn loai = new LoaiMonAn();
        loai.setMaLoaiMonAn(Integer.valueOf(txtMaLoaiMon.getText()));
        loai.setTenLoaiMonAn(txtTenLoaiMon.getText());
        return loai;
    }

