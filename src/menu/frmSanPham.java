    void fillTableMon1() {
        DefaultTableModel model = (DefaultTableModel) tblMonAn.getModel();
        model.setRowCount(0);
        try {
            String tenMon = txtTimKiem.getText();
            List<MonAn> list = mondao.selectTenMon(tenMon);
            for (MonAn mon : list) {
                Object[] rows = {mon.getMaMonAn(),
                    mon.getTenMonAn(),
                    mon.getTenLoaiMonAn(),
                    mon.getGiaTien() + dong,
                    mon.getHinhAnh()};
                model.addRow(rows);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy cập dữ liệu tìm kiếm!");
        }
    }

